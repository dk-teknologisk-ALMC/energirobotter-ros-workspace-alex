class Service:
    """Wrapper omkring én underliggende subprocess + GUI-status."""

    def __init__(self, spec):
        self.spec = spec
        self.proc = None
        self.last_exit_code = None
        # GUI-elementer sættes af LauncherApp under build_ui
        self.status_label = None
        self.start_btn = None
        self.stop_btn = None

    def is_running(self):
        return self.proc is not None and self.proc.poll() is None

    def _log(self, log_fn, message):
        """Send en linje til GUI'ens log med denne service som kilde."""
        log_fn(self.spec["key"], message)

    def start(self, log_fn):
        if self.is_running():
            self._log(log_fn, "kører allerede — ignoreret")
            return
        cmd = self.spec["command"]
        if self.spec.get("needs_ros_source"):
            cmd = f"{LOCAL_ROS_SOURCES} && {cmd}"

        self._log(log_fn, "starter")
        # setsid -> egen pgroup vi kan SIGINT'e samlet.
        env = os.environ.copy()
        if ASKPASS_PATH:
            # Tving ssh + sudo til at bruge zenity-prompten.
            env.setdefault("SSH_ASKPASS", ASKPASS_PATH)
            env.setdefault("SSH_ASKPASS_REQUIRE", "force")
            env.setdefault("DISPLAY", ":0")
            # sudo -A bruger SUDO_ASKPASS.
            env.setdefault("SUDO_ASKPASS", ASKPASS_PATH)
        try:
            self.proc = subprocess.Popen(
                ["bash", "-c", cmd],
                stdout=subprocess.PIPE,
                stderr=subprocess.STDOUT,
                stdin=subprocess.DEVNULL,
                preexec_fn=os.setsid,
                text=True,
                bufsize=1,
                env=env,
            )
        except Exception as e:
            self._log(log_fn, f"kunne ikke starte: {e}")
            return
        threading.Thread(target=self._pump, args=(log_fn,), daemon=True).start()

    def stop(self, log_fn, hard=False):
        if not self.is_running():
            return
        sig = signal.SIGTERM if hard else signal.SIGINT

        # Fallback for processer der ikke kan signaleres direkte (fx pkexec).
        stop_cmd = self.spec.get("stop_command")
        if stop_cmd:
            self._log(log_fn, f"stopper via stop_command: {stop_cmd}")
            try:
                subprocess.Popen(["bash", "-c", stop_cmd])
            except Exception as e:
                self._log(log_fn, f"stop_command fejl: {e}")
            return

        self._log(
            log_fn,
            f"stopper ({'SIGTERM' if hard else 'SIGINT'} til pgroup {self.proc.pid})",
        )
        try:
            os.killpg(os.getpgid(self.proc.pid), sig)
        except ProcessLookupError:
            pass
        except PermissionError as e:
            # Sker ved root-ejet proces — log og fortsaet.
            self._log(
                log_fn,
                f"kunne ikke signalere ({e}) — processen er privileged. "
                f"Brug evt. 'pkexec pkill ...' manuelt.",
            )

    def _pump(self, log_fn):
        try:
            for line in self.proc.stdout:
                if line.strip():
                    self._log(log_fn, line.rstrip())
        except Exception as e:
            self._log(log_fn, f"log-pump fejl: {e}")
        rc = self.proc.wait()
        self.last_exit_code = rc
        self._log(log_fn, f"afsluttet (exit={rc})")


