    # ------------------------- log/status helpers --------------------------

    def log_msg(self, source_key, message):
        """Thread-safe log-append. source_key="" gaar kun til Alle-fanen."""
        with self._log_lock:
            self._log_queue.append((source_key, message))

    def _flush_log_queue(self):
        """Main-thread tick (50 ms). Batch-flush queue til log-tabs."""
        try:
            with self._log_lock:
                n = min(len(self._log_queue), 200)
                items = [self._log_queue.popleft() for _ in range(n)]
            if items:
                per_tab = {}
                all_lines = []
                for src, msg in items:
                    if src:
                        per_tab.setdefault(src, []).append(msg)
                        all_lines.append(f"[{src}] {msg}")
                    else:
                        all_lines.append(msg)
                for src, lines in per_tab.items():
                    tab = self._get_or_create_tab(src)
                    tab.insert("end", "\n".join(lines) + "\n")
                    tab.see("end")
                self.log_all.insert("end", "\n".join(all_lines) + "\n")
                self.log_all.see("end")
        except tk.TclError:
            # Vinduet er ved at blive destroyed; stop reschedule.
            return
        self.after(50, self._flush_log_queue)

    def _append_log(self, source_key, message):
        # Bevares for evt. main-thread-paths (log_msg-pathen bruger queue).
        if source_key:
            tab = self._get_or_create_tab(source_key)
            tab.insert("end", f"{message}\n")
            tab.see("end")
            self.log_all.insert("end", f"[{source_key}] {message}\n")
        else:
            self.log_all.insert("end", f"{message}\n")
        self.log_all.see("end")

    def _sys_log(self, message):
        """Synkron system-log (kun fra main-thread under build)."""
        self.log_all.insert("end", f"{message}\n")
        self.log_all.see("end")

    def _get_or_create_tab(self, key):
        if key in self.log_tabs:
            return self.log_tabs[key]
        spec = next((s for s in SERVICES if s["key"] == key), None)
        label = spec["label"] if spec else key
        widget = scrolledtext.ScrolledText(
            self.log_notebook, height=14, font=("monospace", 9), wrap="word"
        )
        self.log_notebook.add(widget, text=label)
        self.log_tabs[key] = widget
        return widget

    def _clear_log(self):
        self.log_all.delete("1.0", "end")
        for w in self.log_tabs.values():
            w.delete("1.0", "end")

    def _refresh_status(self):
        for svc in self.services.values():
            if svc.is_running():
                color = COLOR_RUNNING
            elif svc.last_exit_code is not None and svc.last_exit_code not in (
                0, -2, -15, 130, 143
            ):
                # 0 = clean, -2/-15 = vores SIGINT/SIGTERM, 130/143 = unix conv.
                color = COLOR_ERROR
            else:
                color = COLOR_STOPPED
            if svc.status_label is not None:
                svc.status_label.config(fg=color)
        self.after(500, self._refresh_status)

    # ------------------------- handlers ------------------------------------

    def start(self, key):
        self.services[key].start(self.log_msg)

    def stop(self, key):
        self.services[key].stop(self.log_msg)

    def stop_all(self):
        # Animationer foerst saa servoer ikke faar modstridende kommandoer.
        try:
            self.animation_runner.stop(self.log_msg)
        except Exception as e:
            self.log_msg("animation", f"stop fejl: {e}")

        # Soft stop (SIGINT) + hard stop efter 3 s grace.
        for svc in self.services.values():
            try:
                svc.stop(self.log_msg, hard=False)
            except Exception as e:
                # Forhindr en enkelt fejlende service i at blokere shutdown.
                self.log_msg(svc.spec["key"], f"stop fejl: {e}")

        def hard_kill_remaining():
            time.sleep(3.0)
            for svc in self.services.values():
                if svc.is_running():
                    try:
                        svc.stop(self.log_msg, hard=True)
                    except Exception as e:
                        self.log_msg(
                            svc.spec["key"], f"hard stop fejl: {e}"
                        )

        threading.Thread(target=hard_kill_remaining, daemon=True).start()

    def start_demo(self):
        def run():
            for key, delay in DEMO_SEQUENCE:
                self.start(key)
                time.sleep(delay)
            self.log_msg(
                "", "[demo] sekvens færdig — åbn http://localhost:8012 i Quest"
            )

        threading.Thread(target=run, daemon=True).start()

