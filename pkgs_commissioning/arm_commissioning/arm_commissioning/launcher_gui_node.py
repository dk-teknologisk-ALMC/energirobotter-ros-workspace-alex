class Service:
    """Wrapper omkring én underliggende subprocess + GUI-status."""

    def __init__(self, spec):
        self.spec = spec
        self.proc = None
        self.last_exit_code = None
        # GUI-elementer sættes af LauncherApp under build_ui
        self.status_label = None
        self.start_btn = None
        self.stop_btn = None

    def is_running(self):
        return self.proc is not None and self.proc.poll() is None

    def _log(self, log_fn, message):
        """Send en linje til GUI'ens log med denne service som kilde."""
        log_fn(self.spec["key"], message)

    def start(self, log_fn):
        if self.is_running():
            self._log(log_fn, "kører allerede — ignoreret")
            return
        cmd = self.spec["command"]
        if self.spec.get("needs_ros_source"):
            cmd = f"{LOCAL_ROS_SOURCES} && {cmd}"

        self._log(log_fn, "starter")
        # Eksplicit setsid → vi får en pgroup vi kan SIGINT'e samlet
        # uden at ramme launcher-GUI'en selv.
        env = os.environ.copy()
        if ASKPASS_PATH:
            # Tving SSH (og evt. andre verktøjer) til at bruge zenity-prompten
            # i stedet for at fejle på manglende TTY.
            env.setdefault("SSH_ASKPASS", ASKPASS_PATH)
            env.setdefault("SSH_ASKPASS_REQUIRE", "force")
            env.setdefault("DISPLAY", ":0")
            # 'sudo -A' bruger SUDO_ASKPASS — samme zenity-script.
            env.setdefault("SUDO_ASKPASS", ASKPASS_PATH)
        try:
            self.proc = subprocess.Popen(
                ["bash", "-c", cmd],
                stdout=subprocess.PIPE,
                stderr=subprocess.STDOUT,
                stdin=subprocess.DEVNULL,
                preexec_fn=os.setsid,
                text=True,
                bufsize=1,
                env=env,
            )
        except Exception as e:
            self._log(log_fn, f"kunne ikke starte: {e}")
            return
        threading.Thread(target=self._pump, args=(log_fn,), daemon=True).start()

    def stop(self, log_fn, hard=False):
        if not self.is_running():
            return
        sig = signal.SIGTERM if hard else signal.SIGINT

        # Hvis servicen kan ikke signaleres direkte (fx root-ejet via
        # pkexec), bruges en eksplicit stop-kommando i stedet.
        stop_cmd = self.spec.get("stop_command")
        if stop_cmd:
            self._log(log_fn, f"stopper via stop_command: {stop_cmd}")
            try:
                subprocess.Popen(["bash", "-c", stop_cmd])
            except Exception as e:
                self._log(log_fn, f"stop_command fejl: {e}")
            return

        self._log(
            log_fn,
            f"stopper ({'SIGTERM' if hard else 'SIGINT'} til pgroup {self.proc.pid})",
        )
        try:
            os.killpg(os.getpgid(self.proc.pid), sig)
        except ProcessLookupError:
            pass
        except PermissionError as e:
            # Sker hvis processen kører som root (pkexec) og vi prøver at
            # signalere som almindelig bruger. Logges, men crasher ikke.
            self._log(
                log_fn,
                f"kunne ikke signalere ({e}) — processen er privileged. "
                f"Brug evt. 'pkexec pkill ...' manuelt.",
            )

    def _pump(self, log_fn):
        try:
            for line in self.proc.stdout:
                # Trim helt blanke linjer for at holde loggen kompakt
                if line.strip():
                    self._log(log_fn, line.rstrip())
        except Exception as e:
            self._log(log_fn, f"log-pump fejl: {e}")
        rc = self.proc.wait()
        self.last_exit_code = rc
        self._log(log_fn, f"afsluttet (exit={rc})")


# ---------------------------------------------------------------------------
# Animation runner
#
# Animationer afspilles fire-and-forget over SSH; kun én ad gangen for at
# undgå at flere animation_player_node-instanser kommanderer servoer
# samtidigt. Hvis der allerede kører en animation, stoppes den først.
# Output havner i "animation"-fanen i log-notebooken.
# ---------------------------------------------------------------------------


class AnimationRunner:
    LOG_KEY = "animation"

    def __init__(self):
        self.proc = None
        self.current_csv = None

    def is_running(self):
        return self.proc is not None and self.proc.poll() is None

    def play(self, csv_basename, log_fn, loop=False):
        if self.is_running():
            log_fn(
                self.LOG_KEY,
                f"stopper {self.current_csv} for at starte {csv_basename}",
            )
            self._kill()
            # Lokal SSH dies med det samme; men det remote ros2 run
            # hører kun SIGHUP via sshd-lukning, og det er upaalideligt
            # naar man relauncher hurtigt. Send eksplicit pkill over SSH
            # — genbruger ControlMaster-socketen saa det er <100ms.
            self._remote_kill(log_fn)
            # Kort grace saa servoer slipper før ny node forbinder
            time.sleep(0.4)

        # $(ros2 pkg prefix ...) bliver eksprimeret af *remote* shell
        # — lokal single-quote forhindrer lokal eksprimering, og remote
        # bash ser strengen ren og parser den selv. Det gør kommandoen
        # robust uanset hvor energinet workspace ligger på Jetson.
        #
        # 'exec' erstatter remote bash med selve ros2-processen, så naar
        # SSH-kanalen lukker (SIGHUP fra sshd) rammer signalet rclpy
        # *direkte* i stedet for at skulle propageres gennem en bash-
        # parent. Uden 'exec' overlevede animation_player_node typisk
        # 30+ sekunder efter Stop blev klikket — servoerne fortsatte
        # animationen mens man troede den var stoppet.
        #
        # 'loop:=true/false' siger til animation_player_node om CSV'en
        # skal afspilles én gang og afslutte ved EOF, eller wrapper
        # uendeligt. Sekvenser (animation_* / mimic_*) afspilles loop=true
        # indtil man stopper dem; idle/gestus/poser/test loop=false.
        loop_arg = "true" if loop else "false"
        cmd = (
            f"ssh{SSH_OPTS} -tt {JETSON_HOST} "
            f"'{JETSON_SOURCES} && "
            f"exec ros2 run animation_player animation_player_node --ros-args "
            f"-p csv_file_path:=$(ros2 pkg prefix energirobotter_bringup)"
            f"/share/energirobotter_bringup/animations/{csv_basename}.csv "
            f"-p fps:=24 -p loop:={loop_arg}'"
        )
        log_fn(self.LOG_KEY, f"start {csv_basename} (loop={loop_arg})")
        env = os.environ.copy()
        if ASKPASS_PATH:
            env.setdefault("SSH_ASKPASS", ASKPASS_PATH)
            env.setdefault("SSH_ASKPASS_REQUIRE", "force")
            env.setdefault("DISPLAY", ":0")
            env.setdefault("SUDO_ASKPASS", ASKPASS_PATH)
        try:
            self.proc = subprocess.Popen(
                ["bash", "-c", cmd],
                stdout=subprocess.PIPE,
                stderr=subprocess.STDOUT,
                stdin=subprocess.DEVNULL,
                preexec_fn=os.setsid,
                text=True,
                bufsize=1,
                env=env,
            )
        except Exception as e:
            log_fn(self.LOG_KEY, f"kunne ikke starte {csv_basename}: {e}")
            return
        self.current_csv = csv_basename
        threading.Thread(
            target=self._pump, args=(log_fn,), daemon=True
        ).start()

    def stop(self, log_fn):
        if not self.is_running():
            # Selv hvis vi tror processen er død kan en remote
            # animation_player_node-zombie stadig kommandere servoerne.
            # Send altid et remote pkill for at vaere sikker.
            self._remote_kill(log_fn)
            return
        log_fn(self.LOG_KEY, f"stopper {self.current_csv}")
        self._kill()
        self._remote_kill(log_fn)

    def _kill(self):
        try:
            os.killpg(os.getpgid(self.proc.pid), signal.SIGINT)
        except (ProcessLookupError, PermissionError):
            pass

    def _remote_kill(self, log_fn):
        """Send 'pkill -INT animation_player_node' over SSH til Jetson.
        Genbruger ControlMaster-socketen saa det er ~instant. Fire-and-
        forget; vi venter ikke paa svar."""
        cmd = (
            f"ssh{SSH_OPTS} {JETSON_HOST} "
            f"'pkill -INT -f animation_player_node || true'"
        )
        env = os.environ.copy()
        if ASKPASS_PATH:
            env.setdefault("SSH_ASKPASS", ASKPASS_PATH)
            env.setdefault("SSH_ASKPASS_REQUIRE", "force")
            env.setdefault("DISPLAY", ":0")
        try:
            subprocess.Popen(
                ["bash", "-c", cmd],
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                stdin=subprocess.DEVNULL,
                env=env,
            )
        except Exception as e:
            log_fn(self.LOG_KEY, f"remote pkill fejl: {e}")

    def _pump(self, log_fn):
        try:
            for line in self.proc.stdout:
                if line.strip():
                    log_fn(self.LOG_KEY, line.rstrip())
        except Exception as e:
            log_fn(self.LOG_KEY, f"log-pump fejl: {e}")
        rc = self.proc.wait()
        log_fn(self.LOG_KEY, f"{self.current_csv} afsluttet (exit={rc})")


