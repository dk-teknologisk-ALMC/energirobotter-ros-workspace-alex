class AnimationRunner:
    LOG_KEY = "animation"

    def __init__(self):
        self.proc = None
        self.current_csv = None

    def is_running(self):
        return self.proc is not None and self.proc.poll() is None

    def play(self, csv_basename, log_fn, loop=False):
        if self.is_running():
            log_fn(
                self.LOG_KEY,
                f"stopper {self.current_csv} for at starte {csv_basename}",
            )
            self._kill()
            # Eksplicit pkill over SSH — SIGHUP via sshd er upaalideligt.
            self._remote_kill(log_fn)
            # Kort grace saa servoer slipper før ny node forbinder
            time.sleep(0.4)

        # 'exec' erstatter remote bash med ros2-processen saa SIGHUP rammer
        # rclpy direkte. 'loop:=' styrer om CSV'en gentages eller stopper ved EOF.
        loop_arg = "true" if loop else "false"
        cmd = (
            f"ssh{SSH_OPTS} -tt {JETSON_HOST} "
            f"'{JETSON_SOURCES} && "
            f"exec ros2 run animation_player animation_player_node --ros-args "
            f"-p csv_file_path:=$(ros2 pkg prefix energirobotter_bringup)"
            f"/share/energirobotter_bringup/animations/{csv_basename}.csv "
            f"-p fps:=24 -p loop:={loop_arg}'"
        )
        log_fn(self.LOG_KEY, f"start {csv_basename} (loop={loop_arg})")
        env = os.environ.copy()
        if ASKPASS_PATH:
            env.setdefault("SSH_ASKPASS", ASKPASS_PATH)
            env.setdefault("SSH_ASKPASS_REQUIRE", "force")
            env.setdefault("DISPLAY", ":0")
            env.setdefault("SUDO_ASKPASS", ASKPASS_PATH)
        try:
            self.proc = subprocess.Popen(
                ["bash", "-c", cmd],
                stdout=subprocess.PIPE,
                stderr=subprocess.STDOUT,
                stdin=subprocess.DEVNULL,
                preexec_fn=os.setsid,
                text=True,
                bufsize=1,
                env=env,
            )
        except Exception as e:
            log_fn(self.LOG_KEY, f"kunne ikke starte {csv_basename}: {e}")
            return
        self.current_csv = csv_basename
        threading.Thread(
            target=self._pump, args=(log_fn,), daemon=True
        ).start()

    def stop(self, log_fn):
        if not self.is_running():
            # Lokal proces død, men en remote zombie kan stadig leve.
            self._remote_kill(log_fn)
            return
        log_fn(self.LOG_KEY, f"stopper {self.current_csv}")
        self._kill()
        self._remote_kill(log_fn)

    def _kill(self):
        try:
            os.killpg(os.getpgid(self.proc.pid), signal.SIGINT)
        except (ProcessLookupError, PermissionError):
            pass

    def _remote_kill(self, log_fn):
        """Send 'pkill -INT animation_player_node' over SSH til Jetson."""
        cmd = (
            f"ssh{SSH_OPTS} {JETSON_HOST} "
            f"'pkill -INT -f animation_player_node || true'"
        )
        env = os.environ.copy()
        if ASKPASS_PATH:
            env.setdefault("SSH_ASKPASS", ASKPASS_PATH)
            env.setdefault("SSH_ASKPASS_REQUIRE", "force")
            env.setdefault("DISPLAY", ":0")
        try:
            subprocess.Popen(
                ["bash", "-c", cmd],
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                stdin=subprocess.DEVNULL,
                env=env,
            )
        except Exception as e:
            log_fn(self.LOG_KEY, f"remote pkill fejl: {e}")

    def _pump(self, log_fn):
        try:
            for line in self.proc.stdout:
                if line.strip():
                    log_fn(self.LOG_KEY, line.rstrip())
        except Exception as e:
            log_fn(self.LOG_KEY, f"log-pump fejl: {e}")
        rc = self.proc.wait()
        log_fn(self.LOG_KEY, f"{self.current_csv} afsluttet (exit={rc})")


