# SSH kan ikke prompte for password gennem en non-TTY subprocess. I stedet
# bruger den $SSH_ASKPASS — en eksekverbar fil der printer password til
# stdout. Vi genererer en lille zenity-wrapper i /tmp ved opstart, og
# spawner SSH-services med SSH_ASKPASS_REQUIRE=force + stdin=DEVNULL
# så askpass-stien altid bliver brugt.

_ASKPASS_SCRIPT_BODY = """#!/bin/bash
# Auto-generated by arm_commissioning launcher_gui.
exec zenity --password --title="${1:-SSH password}" 2>/dev/null
"""


def _ensure_askpass_script():
    path = "/tmp/launcher_gui_askpass.sh"
    try:
        with open(path, "w") as f:
            f.write(_ASKPASS_SCRIPT_BODY)
        os.chmod(path, 0o700)
        return path
    except OSError:
        return None


